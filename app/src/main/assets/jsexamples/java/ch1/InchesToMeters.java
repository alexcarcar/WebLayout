class InchesToMeters {
	private static final double oneMeterInInches = 39.37;

	public static void main(String[] args) {
		double inchSize = 1/oneMeterInInches;
		double footSize = 12*inchSize;
		for (int foot=0; foot<12; foot++) {
			for (int inch=0; inch<12; inch++) {
				String feet = foot + "'" + inch + '"';
				String inches = (foot*12 + inch) + " in";
				double meters = foot*footSize + inch*inchSize;
				System.out.printf("%-10s%-10s%.2f m\n", feet, inches, meters);
			}
			System.out.println();
		}
	}
}

